    /**
     * Genera el texto HTML con avatar e icono para mostrar en las etiquetas de jugador
     */
    private String obtenerTextoConAvatar(Jugador jugador) {
        if (jugador.avatar != null && !jugador.avatar.isEmpty()) {
            // Intenta cargar la imagen del avatar
            try {
                var imgUrl = getClass().getResource(jugador.avatar);
                if (imgUrl != null) {
                    // Retorna HTML con imagen pequeña junto al nombre
                    return "<html><img src='" + imgUrl + "' width='16' height='16'/> " + jugador.nombre + "</html>";
                }
            } catch (Exception ignored) {
                // Si falla, solo retorna el nombre
            }
        }
        return jugador.nombre;
    }
    
    private void wireEvents() {
        btnAbandonar.addActionListener(e -> {
            if (controlador != null) {
                controlador.desconectar();
            }
            this.dispose(); // Cierra la ventana del juego
            System.exit(0); // Asegura que la aplicación se cierre completamente
        });

        btnTirarDado.addActionListener(e -> {
            if (controlador != null) {
                controlador.tirar(miId); // Llamamos al método correcto con el ID del jugador
            }
        });
        
        // Configuramos el listener para cuando se mueva una ficha en el tablero
        ((com.mycompany.parchismvc.View.UtilsFront.FondoBGTablero) JPTablero).setOnMoveListener((indiceFicha, idCasillaDestino) -> {
            if (controlador != null) {
                controlador.mover(miId, indiceFicha);
                // Limpiamos los resaltados inmediatamente después de enviar el movimiento.
                ((com.mycompany.parchismvc.View.UtilsFront.FondoBGTablero) JPTablero).limpiarResaltados();
            }
        });
    }

