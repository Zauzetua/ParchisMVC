    public void actualizarJugadores(List<Jugador> jugadores, UUID turnoDe, UUID yo) {
        // Limpiar todas las etiquetas de las bases
        lblJugadorRojo.setText("");
        lblJugadorAzul.setText("");
        lblJugadorVerde.setText("");
        lblJugadorAmarillo.setText("");
        
        lblNickName.setText(""); // Limpiar el nombre del turno anterior
        
        // Asignar nombres y avatares a las etiquetas de color correspondientes
        for (Jugador jugador : jugadores) {
            // Poner el nombre y avatar en la base de su color
            if (jugador.color != null) {
                String textoConAvatar = obtenerTextoConAvatar(jugador);
                switch (jugador.color) {
                    case ROJO -> lblJugadorRojo.setText(textoConAvatar);
                    case AZUL -> lblJugadorAzul.setText(textoConAvatar);
                    case VERDE -> lblJugadorVerde.setText(textoConAvatar);
                    case AMARILLO -> lblJugadorAmarillo.setText(textoConAvatar);
                }
                
                // Guardamos el color del jugador para usarlo después
                if (jugador.id != null && jugador.color != null) {
                    mapaColoresJugadores.put(jugador.id, jugador.color);
                }
            }

            // Si este es el jugador en turno, actualiza lblNickName
            if (turnoDe != null && turnoDe.equals(jugador.id)) {
                lblNickName.setText("Turno de: " + jugador.nombre);
            }
        }

        // Activar el botón de tirar dado solo si es mi turno.
        // Esta lógica debe ir FUERA del bucle para que se evalúe una sola vez.
        if (yo != null && turnoDe != null) {
            btnTirarDado.setEnabled(yo.equals(turnoDe));
        }
    }

